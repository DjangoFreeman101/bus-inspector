@app.get("/stations")
def get_stations(
    lat_min: float = None,
    lat_max: float = None,
    lon_min: float = None,
    lon_max: float = None
):
    conn = get_db()
    now = time.time()
    cutoff = now - WINDOW_SECONDS

    # Get all reports in last 5 minutes
    rows = conn.execute(
        "SELECT station_id, has_inspector FROM reports WHERE reported_at >= ?",
        (cutoff,)
    ).fetchall()
    conn.close()

    # Count yes/no votes per station
    votes = {}
    for row in rows:
        sid = row["station_id"]
        if sid not in votes:
            votes[sid] = {"yes": 0, "no": 0}
        if row["has_inspector"]:
            votes[sid]["yes"] += 1
        else:
            votes[sid]["no"] += 1

    # Filter stations by bounding box if provided
    stations = STATIONS
    if lat_min is not None and lat_max is not None and lon_min is not None and lon_max is not None:
        stations = [s for s in STATIONS if lat_min <= s["lat"] <= lat_max and lon_min <= s["lon"] <= lon_max]

    result = []
    for s in stations:
        v = votes.get(s["id"])
        if v:
            has_inspector = v["yes"] > v["no"]
            total = v["yes"] + v["no"]
        else:
            has_inspector = None
            total = 0
        result.append({
            **s,
            "has_inspector": has_inspector,
            "yes_votes": v["yes"] if v else 0,
            "no_votes": v["no"] if v else 0,
            "total_votes": total,
        })
    return result

@app.post("/report")
def post_report(report: Report):
    conn = get_db()
    conn.execute("""
        INSERT INTO reports (user_id, station_id, has_inspector, reported_at)
        VALUES (?, ?, ?, ?)
        ON CONFLICT(user_id, station_id) DO UPDATE SET
            has_inspector = excluded.has_inspector,
            reported_at = excluded.reported_at
    """, (report.user_id, report.station_id, int(report.has_inspector), time.time()))
    conn.commit()
    conn.close()
    return {"ok": True}
